/* ─────────────────────────────────────────────
   WAVEFORM SNAPSHOT (static, per track)
───────────────────────────────────────────── */
function genWaveSnap(seed) {
  const N = 60; let v = 0.5; const d = [];
  for (let i = 0; i < N; i++) {
    v = Math.max(0.08, Math.min(1, v + Math.sin(seed * i + i * .4) * .18 + (Math.random() - .5) * .22));
    d.push(v);
  }
  return d;
}
/* ─────────────────────────────────────────────
   CANVAS DRAW — two modes:
   1. Static waveform (when paused / no audio ctx)
   2. Live frequency visualiser (when playing)
───────────────────────────────────────────── */
const canvas = document.getElementById("vis-canvas");
const ctx2d  = canvas.getContext("2d");
function drawCanvas() {
  const W = canvas.parentElement.clientWidth || 400;
  const H = 40;
  canvas.width  = W * 2; canvas.height = H * 2;
  canvas.style.width = W + "px"; canvas.style.height = H + "px";
  ctx2d.scale(2, 2);
  ctx2d.clearRect(0, 0, W, H);
  const color = tracks[curIdx] ? tracks[curIdx].bar : "#e8c97a";
  let bars;
  if (analyser && isPlaying) {
    // LIVE visualiser
    const freqData = new Uint8Array(analyser.frequencyBinCount);
    analyser.getByteFrequencyData(freqData);
    bars = Array.from(freqData).slice(0, 60).map(v => v / 255);
  } else {
    // Static waveform snapshot
    bars = waveSnap;
    if (!bars || !bars.length) bars = genWaveSnap(curIdx * 7 + 13);
  }
  const played  = audio.duration ? audio.currentTime / audio.duration : 0;
  const bw      = (W - bars.length) / bars.length;
  bars.forEach((v, i) => {
    const barH = Math.max(2, v * H * 0.82);
    const x    = i * (bw + 1);
    const y    = (H - barH) / 2;
    const done = i / bars.length < played;
    ctx2d.fillStyle = done ? color : "rgba(255,255,255,0.08)";
    ctx2d.beginPath();
    if (ctx2d.roundRect) ctx2d.roundRect(x, y, Math.max(bw, 1), barH, 2);
    else ctx2d.rect(x, y, Math.max(bw, 1), barH);
    ctx2d.fill();
  });
}
function startVisLoop() {
  cancelAnimationFrame(rafId);
  function loop() { drawCanvas(); rafId = requestAnimationFrame(loop); }
  loop();
}
function stopVisLoop() {
  cancelAnimationFrame(rafId);
  drawCanvas();
}
