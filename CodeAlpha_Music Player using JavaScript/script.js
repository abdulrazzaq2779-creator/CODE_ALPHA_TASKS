function updateVol() {
  const v = parseInt(volSl.value);
  audio.volume = v / 100;
  volVal.textContent = v;
  volIc.textContent  = v === 0 ? "🔇" : v < 40 ? "🔉" : "🔊";
  volSl.style.background = `linear-gradient(90deg,rgba(255,255,255,.4) ${v}%,rgba(255,255,255,.08) ${v}%)`;
}
volSl.addEventListener("input", updateVol);
/* ─────────────────────────────────────────────
   FILE UPLOAD — play local mp3/wav/ogg files
───────────────────────────────────────────── */
fileInput.addEventListener("change", e => {
  const files = Array.from(e.target.files);
  if (!files.length) return;
  const newTracks = files.map((f, i) => {
    const url  = URL.createObjectURL(f);
    const name = f.name.replace(/\.[^.]+$/, "").replace(/[-_]+/g, " ");
    const idx  = (tracks.length + i) % 5;
    return {
      name,
      artist: "Your Music",
      src:    url,
      emoji:  EMOJIS[idx],
      bg:     COLORS.bg[idx],
      bar:    COLORS.bar[idx],
      snap:   genWaveSnap((tracks.length + i) * 7 + 99),
      dur:    null,
    };
  });
  const startAt = tracks.length;
  tracks.push(...newTracks);
  buildList();
  statusBadge.style.display = "inline";
  statusBadge.textContent   = files.length + " file" + (files.length > 1 ? "s" : "") + " added";
  setTimeout(() => statusBadge.style.display = "none", 3000);
  // Auto-play first uploaded track
  loadTrack(startAt);
  startPlay();
});
