/* ─────────────────────────────────────────────
   UTILITIES
───────────────────────────────────────────── */
function fmt(s) {
  if (!isFinite(s) || s < 0) return "0:00";
  return Math.floor(s/60) + ":" + String(Math.floor(s%60)).padStart(2,"0");
}
/* ─────────────────────────────────────────────
   BUILD PLAYLIST UI
───────────────────────────────────────────── */
function buildList() {
  const list = $("track-list");
  list.innerHTML = "";
  $("track-count").textContent = tracks.length + " track" + (tracks.length !== 1 ? "s" : "");
  tracks.forEach((t, i) => {
    const row = document.createElement("div");
    row.className = "track-item" + (i === curIdx ? " active" : "");
    row.innerHTML = `
      <span class="track-index">${String(i+1).padStart(2,"0")}</span>
      <div class="track-thumb" style="background:${t.bg}">${t.emoji}</div>
      <div class="track-info">
        <div class="track-name">${t.name}</div>
        <div class="track-artist">${t.artist}</div>
      </div>
      <span class="track-dur">${t.dur ? fmt(t.dur) : "--:--"}</span>`;
    row.onclick = () => { loadTrack(i); startPlay(); };
    list.appendChild(row);
  });
}
/* ─────────────────────────────────────────────
   LOAD TRACK
───────────────────────────────────────────── */
function loadTrack(i) {
  curIdx = i;
  const t = tracks[i];
  dispName.textContent = t.name;
  dispArt.textContent  = t.artist.toUpperCase();
  disc.style.background = t.bg;
  discEmoji.textContent = t.emoji;
  tCur.textContent = "0:00";
  tDur.textContent = t.dur ? fmt(t.dur) : "--:--";
  progFill.style.width = "0%";
  waveSnap = t.snap || genWaveSnap(i * 7 + 13);
  syncLike();
  buildList();
  if (t.src) {
    audio.src = t.src;
    audio.load();
  }
  drawCanvas();
}
/* ─────────────────────────────────────────────
   PLAY / PAUSE
───────────────────────────────────────────── */
function startPlay() {
  if (!tracks.length) return;
  initAudioContext();
  if (audioCtx.state === "suspended") audioCtx.resume();
  audio.play().then(() => {
    isPlaying = true;
    disc.classList.add("spinning");
    disc.classList.remove("paused-spin");
    btnPlay.textContent = "⏸";
    liveDot.classList.add("on");
    startVisLoop();
  }).catch(e => {
    console.warn("Playback error:", e);
  });
}
function pausePlay() {
  audio.pause();
  isPlaying = false;
  disc.classList.remove("spinning");
  disc.classList.add("paused-spin");
  btnPlay.textContent = "▶";
  liveDot.classList.remove("on");
  stopVisLoop();
}
function togglePlay() {
  if (!tracks.length) return;
  isPlaying ? pausePlay() : startPlay();
}
/* ─────────────────────────────────────────────
   AUDIO EVENTS
───────────────────────────────────────────── */
audio.addEventListener("loadedmetadata", () => {
  tDur.textContent = fmt(audio.duration);
  if (tracks[curIdx]) {
    tracks[curIdx].dur = audio.duration;
    buildList();
  }
});
audio.addEventListener("timeupdate", () => {
  if (!audio.duration) return;
  const p = audio.currentTime / audio.duration;
  progFill.style.width = (p * 100) + "%";
  tCur.textContent = fmt(audio.currentTime);
  if (!isPlaying) drawCanvas(); // update waveform fill while seeking
});
audio.addEventListener("ended", () => {
  if (repeatOn) { audio.currentTime = 0; startPlay(); return; }
  const next = shuffleOn
    ? Math.floor(Math.random() * tracks.length)
    : (curIdx + 1) % tracks.length;
  if (!shuffleOn && curIdx === tracks.length - 1) { pausePlay(); return; }
  loadTrack(next); startPlay();
});
/* ─────────────────────────────────────────────
   SEEK
───────────────────────────────────────────── */
function seek(e, el) {
  if (!audio.duration) return;
  const r = el.getBoundingClientRect();
  audio.currentTime = Math.max(0, Math.min(1, (e.clientX - r.left) / r.width)) * audio.duration;
}
progBar.addEventListener("click", e => seek(e, progBar));
$("vis-zone").addEventListener("click", e => seek(e, $("vis-zone")));
